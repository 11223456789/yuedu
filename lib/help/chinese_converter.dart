    switch (mode) {
      case ChineseConvertMode.none:
        return text;
      case ChineseConvertMode.simplifiedToTraditional:
        return toTraditional(text);
      case ChineseConvertMode.traditionalToSimplified:
        return toSimplified(text);
    }
  }
